//8 Operadores bit a bit

var x = 2;
var y = 5;

console.log(`Números: ${x} y ${y}`);
console.log("AND (&):", x & y, "// Bits en común");
console.log("OR (|):", x | y, "// Al menos un bit activo");
console.log("XOR (^):", x ^ y, "// Bits diferentes");
console.log("NOT (~):", ~x, "// Invierte todos los bits");
